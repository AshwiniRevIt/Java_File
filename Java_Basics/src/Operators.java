
public class Operators {

	public static void main(String[] args) {
		int a =5; int b=10;
		int c=4; int d = 6;
		System.out.println(a++);
		System.out.println(++a);
		System.out.println(b--);
		System.out.println(--b);
		System.out.println(c++);
		System.out.println(c++);
		System.out.println(--d);
		System.out.println(--d);
		

	}

}
